# sets are collection of non-repetitive elements
# Contains Unique Value

# To create set using set function
# Empty set
s = set()
print(type(s))
print(s)

# Add element to empty set
s.add(2)
print(s)
# O/p : {1}

s.add(2)
print(s)
# O/P : {1, 2, 1}

# Properties of sets
# 1. sets are un-ordered
# 2. sets are un-indexed
# 3. there is no way to changes elements in sets
# 4. sets cannot contain duplicate values


